import { isEmpty } from 'class-validator';

export function removeAccents(str: string) {
  return str
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/đ/g, 'd')
    .replace(/Đ/g, 'D')
    .replace(/[^a-zA-Z0-9]/g, ' ')
    .trim()
    .replace(/\s+/g, '_')
    .replace(/_+/g, '_')
    .replace(',', '_')
    .toUpperCase();
}

export function toAlias(str: string) {
  str = str
    .trim()
    .toLowerCase()
    .split(' ')
    .filter((c) => !isEmpty(c))
    .map((sub) => {
      sub = sub.replace(/à|á|ạ|ả|ã|â|ầ|ấ|ậ|ẩ|ẫ|ă|ằ|ắ|ặ|ẳ|ẵ/g, 'a');
      sub = sub.replace(/è|é|ẹ|ẻ|ẽ|ê|ề|ế|ệ|ể|ễ/g, 'e');
      sub = sub.replace(/ì|í|ị|ỉ|ĩ/g, 'i');
      sub = sub.replace(/ò|ó|ọ|ỏ|õ|ô|ồ|ố|ộ|ổ|ỗ|ơ|ờ|ớ|ợ|ở|ỡ/g, 'o');
      sub = sub.replace(/ù|ú|ụ|ủ|ũ|ư|ừ|ứ|ự|ử|ữ/g, 'u');
      sub = sub.replace(/ỳ|ý|ỵ|ỷ|ỹ/g, 'y');
      sub = sub.replace(/đ/g, 'd');
      // Some system encode vietnamese combining accent as individual utf-8 characters
      sub = sub.replace(/\u0300|\u0301|\u0303|\u0309|\u0323/g, ''); // Huyền sắc hỏi ngã nặng
      sub = sub.replace(/\u02C6|\u0306|\u031B/g, ''); // Â, Ê, Ă, Ơ, Ư
      sub = sub.replace(',', '_'); // Â, Ê, Ă, Ơ, Ư
      sub = sub.replace(';', '_'); // Â, Ê, Ă, Ơ, Ư
      return sub;
    })
    .join('_')
    .toUpperCase();
  return str;
}
